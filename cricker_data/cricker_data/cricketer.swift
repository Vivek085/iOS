import Foundation

class cricketer{
    var name:String
    var age:Int
    var match:Int
    var innings:Int
    var score:Int
    var notoutn:Int
    
    init(name: String, age: Int, match: Int, innings: Int, score: Int, notoutn: Int) {
        self.name = name
        self.age = age
        self.match = match
        self.innings = innings
        self.score = score
        self.notoutn = notoutn
    }
    
}
